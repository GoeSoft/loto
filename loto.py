# Убедимся, что у нас достаточно элементов
assert len(MUSIC_ITEMS) >= 25, "Нужно минимум 25 музыкальных элементов"

def generate_ticket(pool, size=9):
    """Генерирует один билет из `size` уникальных элементов из `pool`."""
    return tuple(random.sample(pool, size))

def generate_unique_tickets(pool, num_tickets=49, size=9):
    """Генерирует `num_tickets` уникальных билетов."""
    tickets = set()
    attempts = 0
    max_attempts = num_tickets * 100  # защита от бесконечного цикла

    while len(tickets) < num_tickets and attempts < max_attempts:
        ticket = generate_ticket(pool, size)
        tickets.add(ticket)
        attempts += 1

    if len(tickets) < num_tickets:
        print(f"⚠️ Предупреждение: сгенерировано только {len(tickets)} уникальных билетов из {num_tickets}")
    return list(tickets)

def print_ticket(ticket, index):
    """Печатает билет в виде сетки 3x3."""
    print(f"\nБилет #{index + 1}")
    print("-" * 30)
    for i in range(0, 9, 3):
        row = " | ".join(f"{item:^10}" for item in ticket[i:i+3])
        print(row)
    print("-" * 30)

